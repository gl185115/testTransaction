package ncr.res.mobilepos.dao.model;

public class MstSizeInfo {
    private String companyId;
    private String storeId;
    private String sizeId;
    private String sizeName;
    private String sizeKanaName;
    private String sizeShortName;
    private String sizeShortKanaName;

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getSizeId() {
        return sizeId;
    }

    public void setSizeId(String sizeId) {
        this.sizeId = sizeId;
    }

    public String getSizeName() {
        return sizeName;
    }

    public void setSizeName(String sizeName) {
        this.sizeName = sizeName;
    }

    public String getSizeKanaName() {
        return sizeKanaName;
    }

    public void setSizeKanaName(String sizeKanaName) {
        this.sizeKanaName = sizeKanaName;
    }

    public String getSizeShortName() {
        return sizeShortName;
    }

    public void setSizeShortName(String sizeShortName) {
        this.sizeShortName = sizeShortName;
    }

    public String getSizeShortKanaName() {
        return sizeShortKanaName;
    }

    public void setSizeShortKanaName(String sizeShortKanaName) {
        this.sizeShortKanaName = sizeShortKanaName;
    }
}
