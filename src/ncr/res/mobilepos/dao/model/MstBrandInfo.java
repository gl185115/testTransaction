package ncr.res.mobilepos.dao.model;

public class MstBrandInfo {

    private String companyId;
    private String storeId;
    private String brandId;
    private String brandName;
    private String brandKanaName;

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getBrandId() {
        return brandId;
    }

    public void setBrandId(String brandId) {
        this.brandId = brandId;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getBrandKanaName() {
        return brandKanaName;
    }

    public void setBrandKanaName(String brandKanaName) {
        this.brandKanaName = brandKanaName;
    }

}
