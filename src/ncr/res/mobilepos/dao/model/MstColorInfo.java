package ncr.res.mobilepos.dao.model;

public class MstColorInfo {

    private String companyId;
    private String storeId;
    private String colorId;
    private String colorName;
    private String colorKanaName;
    private String colorShortName;
    private String colorShortKanaName;

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getColorId() {
        return colorId;
    }

    public void setColorId(String colorId) {
        this.colorId = colorId;
    }

    public String getColorName() {
        return colorName;
    }

    public void setColorName(String colorName) {
        this.colorName = colorName;
    }

    public String getColorKanaName() {
        return colorKanaName;
    }

    public void setColorKanaName(String colorKanaName) {
        this.colorKanaName = colorKanaName;
    }

    public String getColorShortName() {
        return colorShortName;
    }

    public void setColorShortName(String colorShortName) {
        this.colorShortName = colorShortName;
    }

    public String getColorShortKanaName() {
        return colorShortKanaName;
    }

    public void setColorShortKanaName(String colorShortKanaName) {
        this.colorShortKanaName = colorShortKanaName;
    }
}
