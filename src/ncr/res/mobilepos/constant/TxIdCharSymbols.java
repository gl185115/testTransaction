package ncr.res.mobilepos.constant;

/**
 * TxIDCharSymbols is a class that enumerates character symbol of transaction number format.
 */
public class TxIdCharSymbols {
	/**
	 * CompanyId character symbol.
	 */
	public static final String COMPANY = "C";
	/**
	 * StoreId character symbol.
	 */
	public static final String STORE = "B";
	/**
	 * Terminal character symbol.
	 */
	public static final String TERMINAL = "R";
	/**
	 * BusinessDayDate character symbol.
	 */
	public static final String BUSINESSDATE = "YYMMDD";
	/**
	 * SequenceNumber character symbol.
	 */
	public static final String SEQUENCENUMBER = "X";
}
